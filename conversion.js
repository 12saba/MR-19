// Explicit Type Conversion
let numStr = "123";
let numInt = Number(numStr); // Convert string to number
console.log(numInt, typeof numInt); // Output: 123 'number'

// Convert number to string
let num = 456;
let numStr2 = String(num);
console.log(numStr2, typeof numStr2); // Output: "456" 'string'
