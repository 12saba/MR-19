// Define two variables
let a = 10;
let b = 5;

// Addition
let sum = a + b;
console.log(`Addition: ${a} + ${b} = ${sum}`); // Output: Addition: 10 + 5 = 15

// Subtraction
let difference = a - b;
console.log(`Subtraction: ${a} - ${b} = ${difference}`); // Output: Subtraction: 10 - 5 = 5

// Multiplication
let product = a * b;
console.log(`Multiplication: ${a} * ${b} = ${product}`); // Output: Multiplication: 10 * 5 = 50

// Division
let quotient = a / b;
console.log(`Division: ${a} / ${b} = ${quotient}`); // Output: Division: 10 / 5 = 2

// Modulus (Remainder)
let remainder = a % b;
console.log(`Modulus: ${a} % ${b} = ${remainder}`); // Output: Modulus: 10 % 5 = 0

// Increment
a++;
console.log(`Increment a: ${a}`); // Output: Increment a: 11

// Decrement
b--;
console.log(`Decrement b: ${b}`); // Output: Decrement b: 4
